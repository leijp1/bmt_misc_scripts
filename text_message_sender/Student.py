class Student:
	def __init__(self, student_id, name, team):
		self.id = student_id
		self.name = name
		self.team = team

	def set_student_id(student_id):
		self.id = student_id

	def set_student_name(name):
		self.name = name

	def set_team(team):
		self.team = team

	def get_student_id():
		return self.id

	def get_student_name():
		return self.name

	def get_team():
		return self.team